@Slf4j
@Service //需要将该类当成bean并注入，可以使用@Service或者@Component
public class AsyncService {

    //springboot是重新起一个线程，来执行异步任务, 所以线程名不一样
    //springboot内置一个异步线程池，但是这个线程池的容量只有1，为了提高性能自定义一个异步线程池
    @Async("getAsyncExecutor")
    public void asyncProcess() throws InterruptedException {
        log.info("asyn process task, current thread name -> {}, time -> {}",
                Thread.currentThread().getName(), LocalDateTime.now());
        TimeUnit.SECONDS.sleep(2);
    }

    // 有返回值的异步任务
    @Async("getAsyncExecutor")
    public Future<Integer> asyncProcessHasReturn() throws InterruptedException {
        log.info("async process task (has return), current thread name -> {}, time -> {}",
                Thread.currentThread().getName(), LocalDateTime.now());
        TimeUnit.SECONDS.sleep(3);
        return new AsyncResult<>(100);
    }
}
